export const Card = ({ card, isFaceDown }) => {
  const { value, suit } = card;
  const color = suit === "D" || suit === "H" ? "red" : "black";
  const suiteSymbol = type =>
    ({
      'C': Clubs(),
      'D': Diamonds(),
      'H': Hearts(),
      'S': Spades()
    }[type]);
  
  return isFaceDown ? (
    <div class="card_back">
      <img src="../assets/spark_logo_w.svg" alt="" />
    </div>
  ) : (
    <div class={`card ${color}`}>
      <span>{value}</span>
      {suiteSymbol(suit)}
    </div>
  );
};
